                <p class="primeiro-paragrafo">Qual a proposta?</p>
                <p class="terceiro-paragrafo">A educação é uma das áreas que mais vem sendo impactadas pela tecnologia nos últimos anos. Seja pela criação e disponibilização de bibliotecas virtuais, ambientes virtuais de ensino, plataformas de apoio à aprendizagem, aulas à distância como vimos durante a pandemia e entre outras (DOS SANTOS JUNIOR e DA SILVA MONTEIRO, 2020).
                <p class="terceiro-paragrafo">Uma área, em específico, vem apresentando grande crescimento contínuo também na aplicação do uso direcionado para educação, a Inteligência Artificial (IA). Como esse é um tema novo e que teve um grande fomento nos últimos anos, é necessário aprendermos como usá-la a nosso favor e não contra. Isto é, como uma ferramenta, não como um final absoluto.</p>
                <p class="terceiro-paragrafo">É sabido que a grande maioria das pessoas enfrentam condições adversas ao acesso à educação no Brasil. Isso se agrava ainda mais na realidade das escolas públicas do Brasil, que contam com superlotação de classes, falta de investimentos, falta de estrutura e muitos outros problemas, provendo um ambiente muito desfavorável ao ensino e aprendizado (SANTOS, 2005). Nesse sentido, a pergunta motivadora não seria “Porque não aplicar o uso de IA na educação?”, mas sim “Como aplicar a IA na educação de forma a auxiliar professores e alunos?”.</p>
